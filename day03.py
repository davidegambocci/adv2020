def count_trees_along_the_way(lst, right, down):
    step = 0
    count_trees = 0
    for r in range(0, len(lst), down):
        if lst[r][step] == '#':
            count_trees += 1

        step += right
        if step >= len(lst[r]):
            step = step % len(lst[r])

    return count_trees



